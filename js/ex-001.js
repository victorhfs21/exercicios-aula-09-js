for (let i = 1; i <= 10; i++){
    document.getElementById("contagem").innerHTML += i + "<br>";
}

// laço de repetiçôes são blocos dentro de nosso código que será executados até que a condição seja falsa, no caso do FOR ele possui 3 partes:
// for (inicialização da variável; condição; incremento)
// for (let i = 0; i <= 10; i++)
// Inicialização da variável --> aqui colocamos um valor inicial para a variável que servirá para controle de nsso laço. Normalmente o nome dessa variável é i(increment), porém não há problema em usar outro nome
// condição --> teste que será realizado em cada nova passagem do laço, o conteúdo dentro do laço será executado enquanto a condição estiver retornando verdadeiro.
// incremento --> aumento no número da variável. i++ é a mesma coisa que i = i + 1